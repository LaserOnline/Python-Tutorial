
# * การส่งคืนค่าข้อมูลกลับออกไปจากฟังก์ชัน
# * เมื่อฟังก์ชันประมวลผลตามคำสั่งใดๆ เรียบร้อยแล้ว สามารถส่งคืนค่ากลับออกไปจากฟังก์ชัน
# * ได้โดยใช้คำสั่ง return

# * def ชื่อฟังก์ชัน(พารามิเตอร์):
# * คำสั่งการทำงานต่าง ๆ ภายในฟังก์ชัน
# * return ค่าข้อมูล

def plus(num1, num2):
    return num1 + num2
# * ใช้คำสั่ง return ส่งคืนค่าผลบวกกลับออกไปจากฟังก์ชัน


print(plus(5, 5))
print(plus(6, 6))
# * สั่งพิมค่าข้อมูลที่ ฟังก์ชันคืนกลับออกมา
